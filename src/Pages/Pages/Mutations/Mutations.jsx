      facebook: $facebook,
      tiktok: $tiktok,
      headerText: $headerText,
      secondaryText: $secondaryText,
      font: $font,
      twitter: $twitter,
      subscribeText: $subscribeText,
      subscribeSubText: $subscribeSubText,
      headerImage: $headerImage,
      secondaryImage: $secondaryImage,
